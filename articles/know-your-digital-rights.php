                        <header>
                            <h2>Know your Digital Rights</h2>
                            <span class="byline">Written by Yara Fahmy</span>
                        </header>
                        <p>
                            Dr. Richard Stallman, the guru of free programming, speaks of the importance of free software in a teleconference session
                        </p>
                        <p>
                            “Not only does digital technology liberate people, it can also suffocate people,” Dr. Stallman asserted at the start of his
                            session. We all know that developers have implicit control over the programs they create. But
                            why can’t users have access to such power? On the 10th of September, Dr. Richard Stallman shared
                            his story about his fight against proprietary programming with the attendees of ITW’13.
                        </p>
                        <a href="#" class="button alt2 moreArticle">More</a>
                        <div class="extra_info">
                            <p>
                                ‘Individual Control’ over the program can only be gained through 2 types of freedoms. The first is to allow the users to
                                run the program whenever they wish. The second is to study the source code and change it
                                so that it can work in the way the user wants it to. However, Dr. Stallman said that individual
                                freedom isn’t enough because not all users are experienced developers hence don’t know how
                                to exercise these freedoms.
                            </p>
                            <p>
                                The idea of CR was developed by Joseph Mitola at the Defense Advanced Research Projects Agency (DARPA) in the United States.
                            </p>
                            <p>
                                This is where ‘Collective Control’ comes into the picture. Collective control means any group of users can have control over
                                the program. This kind of control requires two supplementary freedoms which are the freedom
                                to redistribute copies of any program and distribute altered versions of the program.
                            </p>
                            <p>
                                If the program does not have those 4 fundamental freedoms, then the users are controlled by the program, and in turn by the
                                owners of the program. This gives the owner bias power. Thereby, non-free programs must be
                                eliminated. That is the cause of the Free Software Movement founded by Dr. Stallman in 1983.
                            </p>
                            <p>
                                Owners can abuse that power to gain advantage over the users. Consequently, nonfree programs can have ‘Malicious Functionality’.
                                Many users think of malware as viruses only. But malware can come in the form of surveillance
                                or spyware, digital handcuffs that limit the usage of the computer, and backdoors which receive
                                commands without the users’ authority.
                            </p>
                            <p>
                                Many of us are not aware of the presence of malicious software in the programs we use. Among them, is Microsoft Windows which
                                has specific spy features that send unauthorised data to servers. Digital Rights Management
                                (DRM) which is the control of data and devices can be found in Microsoft Windows. It also
                                has 2 invisible backdoors. The Universal backdoor, forcefully changes the way the software
                                works without the users’ permission and Sabotage which is sending information about bugs
                                to the US government before fixing them so that they can manipulate those bugs and gain access
                                to private data.
                            </p>
                            <p>
                                Microsoft Windows is not the only malicious software out there in the market. Apple Macintosh carries a more serious problem.
                                Dr.
                            </p>
                            <p>
                                Stallman states that iOS does many nasty things. It has even nastier digital handcuffs than Microsoft Windows because it
                                has more control over the software installed. Users can’t install anything except for applications
                                approved by Apple such as iTunes, Safari etc. These applications are “platforms for censorships”
                                which is against the law. But it isn’t because Apple uses the issue of jail breaking in its
                                defence.
                            </p>
                            <p>
                                “Apple has developed the nastiest digital handcuffs in the history of general purpose computing”
                            </p>
                            <p>
                                Non-free software is mistaken for paid software. Software is said to be free if it attains the 4 essential freedoms. Some
                                softwares that don’t require any payment such as Flash Player and Angry Birds don’t have
                                the 4 freedoms. Smartphones are used as listening devices even when switched off. The only
                                way to stop it from transmitting what it hears is by extracting the battery. However, some
                                phones are developed to transmit illegal data without the need of a battery. Portable phones
                                with GPS can transmit data of your location. Dr. Stallman referred to these kinds of systems
                                as “Mubarak’s Device”. He personally doesn’t own a smartphone because he is not willing for
                                anyone to listen without his consent.
                            </p>
                            <p>
                                With all that malicious software, what can we do to stop it? The truth is, we can do nothing about proprietary software.
                                We can’t identify which ones are malware and see if they are sabotaging us or not. The best
                                solution to this problem is to install free software instead. Dr. Stallman, who started off
                                as an OS developer, wanted to build a 100% free software OS.
                            </p>
                            <p>
                                Of course, he couldn’t do that alone, so he recruited others to work with him. They tried to create a similar OS to Unix,
                                and gave it a recursive acronym GNU which stands for “Gnu’s Not Unix”. He and his team have
                                been working on it for 13 years. In 1992, GNU was almost complete but was missing the Kernel.
                                Linus Trovalds then released Linux which had a Kernel. With both projects merged into one,
                                they created a complete OS. The name of the OS is commonly known as Linux, but Dr. Stallman
                                stressed that the proper name is GNU/Linux.
                            </p>
                            <p>
                                “Please give GNU equal mention because they are fighting for our freedom”
                            </p>
                            <p>
                                Free software is still not sufficient to avoid unapproved surveillance. Governments spy on networks through advertisements
                                on websites. Browsers don’t even warn you if the software you are about to download is free
                                or not if programmed with Javascript. The better alternative is AJAX which blocks the download
                                if the software is non-free. It also sends a complaint to the web managers of the presence
                                of non-free software.
                            </p>
                            <p>
                                You can do more than simply installing free software to your computer. You can help the movement by becoming a speaker. Create
                                awareness among your close circle of friends and family. Dr. Stallman suggests that education
                                must not allow proprietary software. Students will be fascinated if free software is introduced
                                to the educational system because it will encourage them to read and study codes. Reverse
                                engineering is the best way to teach students how to become good programmers. Dr. Stallman
                                concluded his session with the fact that free software is not new, but has been going on
                                for 30 years.
                            </p>
                            <p>
                                “If you have a good relationship with a school, it’s your duty to campaign Free Software”
                            </p>
