                            <h2>Humanizing Technology</h2>
                            <span class="byline">Written by Deena Osman</span>
                        </header>
                        <p>
                            It’s so easy to make technology complicated, but it takes a lot of thought and effort to make it simple. How many times have
                            you almost tripped while looking at your phone? Countless, we bet. We would like you to meet
                            the heads-up version of this experience, also known as “Wearable Technology”. Thanks to wearable
                            technology, the world feels a little more surreal every day; a little more magical. Not to mention,
                            reclaiming our humanity. While designing these technologies, one’s aim should be to design a
                            device that is radically simple and that inspires possibilities all the same.
                        </p>
                        <a href="#" class="button alt2 moreArticle">More</a>
                        <div class="extra_info">
                            <p>
                                Imagine when a simple stone-like gadget can be shaken to say, “I love you.” and held tightly to send a message to someone
                                else’s phone. This may seem like luxury at first sight, however, such technologies can be
                                extremely crucial for people with speech problems, people who have communication issues,
                                and even for those who do not, it is a great way to stay connected with everything when life
                                gets too busy to call or text. Imagine when simply carrying a stone around can do all this
                                magic. Smartstones Touch is an embodiment of this fantasy; a sensory device that is connected
                                to the internet, and up to ten of your favorite people. There is also Smartstones Prose;
                                an integrated app that lets you configure and send spoken messages by simply rubbing your
                                stone, which can be very helpful to people with ALS, because it is a way to interact in all
                                those little ways that do not require typing out a message. Wearable technology has come
                                a long way, and more is yet to come. In the future, we might witness an integration of wearable
                                technology by various platforms. You might even be able to do all this magic and contact
                                your favorite people by a tiny gesture using a piece of regular fabric.
                            </p>
