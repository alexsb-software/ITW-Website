                        <header>
                            <h2>Smart Micro Grid</h2>
                            <span class="byline">Written by Bassma El-Abassy</span>
                        </header>
                        <p>
                            Did you ever wonder why the world thrives for the smarter all the time? Is there an ultimate form for everything that science
                            seeks? Or is it just the limitations that we try to overcome?
                        </p>
                        <p>
                            Thomas Edison once said “I’d put my money on sun and solar energy. What a source of power! I hope we don’t have to wait until
                            oil and coal run out before we tackle that."
                        </p>
                        <a href="#" class="button alt2 moreArticle">More</a>
                        <div class="extra_info">
                            <p>
                                Since the Industrial revolution began in the nineteenth century and the world’s supplies for oil and fuel are in imminent
                                danger where no source could ever satisfy the current demand on fuel which is growing at
                                an alarming rate.
                            </p>
                            <p>
                                To address the Middle East specifically where it produces 30% of the total fuel production; it’s not long before it’s done
                                and the lack of coal production only adds to our challenges in Energy supply.
                            </p>
                            <p>
                                That’s why we have planned and prepared for what we already acknowledge as facts. Take England for instance, its plan seeks
                                to achieve the dependence on renewable energy by 99% in 2050, such a prediction should count
                                as an encouragement to be considered in our future plans.
                            </p>
                            <p>
                                Efficiency, Reliability and Safety are the world’s motives towards smart grid transformation. Along with preservation of
                                cyber secure standards to overcome the costs required and secure the smart grid from any
                                cyber-attack.
                            </p>
                            <p>
                                Smart grid basically is a transformed electricity and distribution network or a grid that uses information and communications
                                technology to gather and act on information to improve the efficiency and reliability of
                                energy production and distribution.
                            </p>
                            <p>
                                What matters most to the consumers is the safety of the network where some may doubt its security. Specially that any leak
                                of users database or financial accounts is a huge to risk to take.
                            </p>
                            <p>
                                The whole generation, distribution and communication processes should occur through a secure medium to avoid any data theft
                                which is why the security of the micro grid must be addressed in early stages of manufacture
                                and design, not added afterwards.
                            </p>
                            <p>
                                The current electric networks are composed of two; a power network and a communications network. To apply the smart grid,
                                we can’t just remove the original network and start a new one from scratch. We need to update
                                it gradually and integrate the system to transform between grids.
                            </p>
                            <p>
                                Achieving a satisfactory and successful operation for this technology is not a one-field task; it needs various majors from
                                power and communication engineers to computer engineers for its security software.
                            </p>
                            <p>
                                The major challenge of integrating the system is the cost of this technology which after all lies on the consumer. That brings
                                us to the need to aware the consumer of the advantages it offers without endangering the
                                safety and security of the communication process.
                            </p>
                            <p>
                                The smart grid technology enables the electric grid to respond intelligently to any variation in power supply or demand.
                                It also offers the consumers the privilege of tracing back their energy consumption thus
                                offering a chance to manage and adjust their energy consumption.
                            </p>
                            <p>
                                There are a few other principal characteristics for the smart grid where the consumer is no longer just a load on the network
                                but he will be an active member and engaged within electricity suppliers’ issues and market.
                                It will surely integrate all generations and storage systems along.
                            </p>
                            <p>
                                It will also create large central power plants for renewable energy as in wind and solar power plants. Along with continuous
                                monitoring to the deficiencies that might happen in the system causing any lack in the power
                                supply.
                            </p>
                            <p>
                                The life style which the smart grids offer is mostly suited to affect the consumers to reduce any wasted energy, reduce consumption
                                and lead them to the best usage of clean energy.
                            </p>
                            <p>
                                The smart meter applied at houses will be the guide of the users to their energy consumption where they use wireless technology
                                to transmit radio frequencies (RF) to provide the communication needed to electric companies.
                                All the information will be updated on a regular basis whether we choose it to be every hour,
                                day or week, instead of getting a bill every month with no way to adjust your usage.
                            </p>
                            <p>
                                Apparently, this is the long-term solution that might be just the answer needed by electricity suppliers all around the globe
                                where its enhancement to the system through transportation and delivery is most beneficial
                                to both; the governments and the consumers which counts as a step forward to overcome some
                                of the challenges of energy production and supplies.
                            </p>
