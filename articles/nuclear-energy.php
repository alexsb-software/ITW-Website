                        <p>
                            العلم اساس تقدم الأمم, فاذا اردنا معرفة مدي تقدم الأمة نظرنا الي مدي اهتمامها بالعلم و البحث العلمي.
                        </p>
                        <p>
                            و اذا ما أرادت أمة النهوض من غفلتها فعليها بمراجعة ماضيها فاتستعيد مجده و تتعلم من اخطاءه.
                        </p>
