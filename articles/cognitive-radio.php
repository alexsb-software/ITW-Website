                        <header>
                            <h2>Cognitive Radio</h2>
                            <span class="byline">Written by Sara Mansour</span>
                        </header>
                        <p>
                            One of the main limitations in nextgeneration wireless systems is bandwidth scarcity. Nowadays, most of the spectrum is allocated
                            to specific applications by the Federal Communications Commission (FCC); however, statistics
                            shows that the spectral utilization is low in most of the reserved ranges. So, trying to use
                            the idle primary (licensed) bandwidth and let the secondary (unlicensed) users to communicate
                            when the channel is free sounds reasonable, and this is known by Interoperability which is the
                            ability of two diverse systems to work together. Also, Interoperability must take place without
                            interference between the users. Arising from those problems, Cognitive Radio (CR) technology
                            has been considered.
                        </p>
                        <a href="#" class="button alt2 moreArticle">More</a>
                        <div class="extra_info">
                            <p>
                                Cognitive radio is a form of wireless communication in which a transceiver (transmitter and receiver) can intelligently detect
                                which communication channels are in use and which are not, and instantly move into vacant
                                channels while avoiding occupied ones. This optimizes the use of available radio-frequency
                                (RF) spectrum while minimizing interference to other users.
                            </p>
                            <p>
                                The idea of CR was developed by Joseph Mitola at the Defense Advanced Research Projects Agency (DARPA) in the United States.
                            </p>
                            <p>
                                The CR System consists mainly of a Cognitive Engine which is perfectly described as the brain of the CR system, Software-Defined
                                Radio (SDR) which is a radio platform of which the functionality is at least partially controlled
                                or implemented in software and some Artificial Intelligence (AI) which constitutes the core
                                controller for a cognitive radio system like case-based reasoning, neural networks, rule-based
                                reasoning, genetic algorithm and fuzzy logic, and the selection process of an AI can substantially
                                affect the performance of the system.
                            </p>
                            <p>
                                CR is now applied in USA and that’s in various fields like Health Care where the FCC established the Wireless Medical Telemetry
                                Services (WMTS) band for the transmission of data related to a patient’s health, and similar
                                reserved channels exist for life-critical communications throughout the world. CR technology
                                is used to dynamically utilize the WMTS frequencies based on the activity patterns of the
                                high priority users, and the quality of service constraints of the patients’ data, while
                                ensuring protection to existing higher priority transmissions and the safe operation of sensitive
                                medical equipment.
                            </p>
                            <p>
                                Also CR contributed in Medicine with the Smart Medicine Container and Wireless Event Detection / Notification System which
                                aims to provide the automatic cross-checking method to increase the credibility of the correct
                                medication delivery for the patients. The status and events of the medical containers are
                                tracked in order to avoid the possible human errors. This system will help the patients and
                                assist the nurses and doctors to record what pills the patients took, when the pills were
                                taken, and how many pills were taken. It can aid nurses in knowing when intravenous drips
                                are consumed. It can be used for the tracking and management of not only medical containers
                                in the hospital environment but other items in smart buildings, factories and supermarkets,
                                etc.
                            </p>
                            <p>
                                Another application is in Agriculture where the deployment of CR in the field can help to improve food yields and enable
                                farmers to better forecast crop yields and production. The use of CR to share data increases
                                the number of farmers profiting from the information. At the local level, CR can provide
                                farmers with useful and beneficial information, such as new farming techniques, weather reports,
                                and crop prices.
                            </p>
                            <p>
                                However, CR is still a research challenge; there are lots of challenges in CR to work on like sensing, security, Geolocation,
                                etc. In CR research, whether you will reach results or no, it’s worth trying and it’s always
                                interesting.
                            </p>
                            <p>
                                Experts predict that in the very near we’re going to use cognitive radio in our daily lives, in our simple daily uses, for
                                example, CR will be used in home automation systems. CR technology will have a great contribution
                                in easing our lives and that’s why it’s very interesting, really interesting to get involved
                                in CR’s work.
                            </p>
                            <p>
                                Virginia Tech University is considered one of the most concerned universities with CR. The Wireless @ VT research group has
                                embarked on an effort to develop a unique testbed named the Virginia Tech cognitive radio
                                network (VT-CORNET), for the development, testing, and evaluation of cognitive engine techniques
                                and cognitive radio network applications. An open cognitive radio network testbed provides
                                the infrastructure for researchers at Virginia Tech and partner institutions to evaluate
                                independently developed cognitive radio engines, sensing techniques, applications, protocols,
                                performance metrics, and algorithms in a real world wireless environment, in contrast to
                                a computer simulation or single node-to-single node environment.
                            </p>
                            <p>
                                Among the researchers at Virginia Tech who are highly involved in CR’s work is an Egyptian researcher called Dr Hazem S.
                                Shatila; he received his Bachelor and Masters degree from the Arab Academy for Science &
                                Tech. (AAST)-Egypt, in 1999 and 2003, respectively. He received his PhD degree from Virginia
                                Tech. University-USA in 2012. Dr Hazem is a member of the IEEE and the author of many journal
                                and conference papers. Dr Hazem’s research interests are in the field of wireless communications,
                                networking, cognitive radio and artificial intelligence. He is a technical manager at Vodafone
                                Egypt and a part of Vodafone’s R&D group.
                            </p>
                            <p>
                                Years ago, CR was considered science fiction, but now it’s one of the most essential technologies – which must be widely
                                used later on – and that’s how CR proved literally that the best way to predict the future
                                is to invent it.
                            </p>
