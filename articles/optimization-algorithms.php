                            <p>
                                On the 2nd day of ITW’15, Dr. Ayman Ragab gave a talk about optimization algorithms, he started by a simple question, “why
                                do we need optimization algorithms?”. Generally, if we have a design problem, our target
                                would be achieving the best performance, but this optimum design is achieved by controlling
                                some design parameters, and it takes a lot of time and effort to pick the right values for
                                these parameters, and hence the need of optimization algorithms was developed.
                            </p>
                            <p>
                                An optimization algorithm is a procedure which is executed iteratively as follows: first, choose design variables, then determine
                                the Decision space (DS) boundaries of the variables, next initialize random variables, then
                                set up a termination condition, and finally simulate your problem, if the termination condition
                                is satisfied this means we’ve reached the final solution, if not we update the random variables
                                according to the optimization algorithm, and then go back to simulating and evaluating the
                                values, and so on.
                            </p>
                            <p>
                                There are two distinct types of optimization algorithms widely used today, Deterministic and Stochastic. Deterministic algorithms
                                have been successfully applied to many engineering problems, however, stochastic algorithms
                                are more popular due to certain properties which deterministic algorithms do not have.
                                <br/>
                                <img style="max-width: 720px;" class="article-element-img" src="images/optimizationAlgorithm2.png" alt="" />
                            </p>
                            <p>
                                Moreover, there are optimization algorithms called evolutionary algorithms which is a sub-field of Artificial intelligence.
                                Particle swarm optimization (PSO) is one of the evolutionary computation techniques that’s
                                based on swarm intelligence introduced in 1995 by Kennedy and Eberhard, where random variables
                                are summed with the change of velocity (Velocity: previous velocity + personal velocity +
                                global velocity). Also, Genetic Algorithm (GA) was developed by John Holland in 1975, its
                                main concept is based on “survival of the fittest”, the good solution survives and the bad
                                solution dies.
                            </p>
                            <p>

                                So what are the applications of this algorithms? First application is Antenna design, optimization algorithms are used to
                                determine the shape and the dimensions of the antenna to radiate as required. Second application
                                is compact broad band sheet absorbers, the best reflection coefficient is produced by an
                                optimization algorithm. There are also a lot of other applications, not only in engineering
                                but also in data analysis and business which reveals the importance of optimization algorithms
                                and the need to always get these algorithms developed.
                            </p>

